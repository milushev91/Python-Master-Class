a = 12 
b = 3 

# Addition
print(a + b)  # Output: 15 

# Subtraction
print(a - b)  # Output: 9

# Multiplication
print(a * b)  # Output: 36

# Division
print(a / b)  # Output: 4.0

# Integer Division: rounds down to the nearest whole number
print(a // b)  # Output: 4

# Modulus: returns the remainder of the division
print(a % b)  # Output: 0
