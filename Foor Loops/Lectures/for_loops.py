parrot = "Norwegian Blue"

#for loop can be used to iterate over every iterable object
#a iterable object is an object that can return its elements one at a time
#for loop begins with the keyword 'for' followed by a variable name that will
#hold each element of the iterable object during each iteration
#then the keyword 'in' followed by the iterable object to be looped over
for character in parrot:
    print(character)
